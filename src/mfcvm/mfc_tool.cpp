	// ÉwÉbÉ_èëÇ´çûÇ›
	if (!fio.Open(m_szPath, Fileio::ReadWrite)) {
		return FALSE;
	}
	if (!fio.Seek(0x400)) {
		return FALSE;
	}
	if (!fio.Write(header, sizeof(header))) {
		return FALSE;
	}

	// ò_óùÉtÉHÅ[É}ÉbÉgÇçsÇÌÇ»Ç¢Ç»ÇÁÅAÇ±Ç±Ç‹Ç≈
