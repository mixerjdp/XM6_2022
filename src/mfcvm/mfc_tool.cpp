BOOL CDiskMakeDlg::OnInitDialog()
{
	CProgressCtrl *pProgress;
	CStatic *pStatic;
	CString strParcent;

	ASSERT(this);
	ASSERT(_tcslen(m_szPath) > 0);
	ASSERT(m_dwSize > 0);

	// äÓñ{ÉNÉâÉX
	CDialog::OnInitDialog();

	// ÉRÉìÉgÉçÅ[Éãèâä˙âª
	pProgress = (CProgressCtrl*)GetDlgItem(IDC_DMAKE_PROGRESS);
	ASSERT(pProgress);
	pProgress->SetRange(0, 100);
	pProgress->SetPos(0);
	pStatic = (CStatic*)GetDlgItem(IDC_DMAKE_STATIC);
	if (m_dwSize & 0xfffff) {
		strParcent.Format(_T("0MB / %d.%1dMB (0%%)"),
							m_dwSize >> 20,
							(m_dwSize & 0xfffff) / 104858);
	}
	else {
		strParcent.Format(_T("0MB / %dMB (0%%)"),
							m_dwSize >> 20);
	}
	pStatic->SetWindowText(strParcent);

	// çÏã∆ÉèÅ[ÉNÇämï€
	try {
		m_pBuffer = new BYTE[ 0x100000 ];
	}
	catch (...) {
		EndDialog(IDOK);
		return FALSE;
	}
	if (!m_pBuffer) {
		EndDialog(IDOK);
		return FALSE;
	}

	// çÏã∆ÉèÅ[ÉNÇÉNÉäÉA
	memset(m_pBuffer, 0, 0x100000);

	// ÉXÉåÉbÉhÇóßÇƒÇÈ
	m_pThread = AfxBeginThread(ThreadFunc, this);
	if (!m_pThread) {
		EndDialog(IDOK);
		return FALSE;
	}

	// É^ÉCÉ}ÇÉCÉìÉXÉgÅ[Éã
	m_nTimerID = SetTimer(IDD_DMAKEDLG, 200, NULL);
	if (!m_nTimerID) {
		EndDialog(IDOK);
		return FALSE;
	}

	return TRUE;
}

