	if (!fio.Write(fat, sizeof(fat))) {
		return FALSE;
	}

	// ÉpÅ[ÉeÉBÉVÉáÉìÉeÅ[ÉuÉãçÏê¨
	strcpy((char*)header, "Human68k");
	header[ 9] = 0x00;
	header[10] = 0x00;
	header[11] = 0x21;
