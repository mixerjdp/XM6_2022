	ASSERT(pWnd);
	pWnd->GetClientRect(&rectSrc);
	pWnd->SetWindowPos(&wndTop, 0, 0, rectSrc.Width(), rectDst.Height(),
								SWP_NOMOVE | SWP_NOZORDER);

	// ÉTÉCÉYÉXÉsÉìÉRÉìÉgÉçÅ[ÉãéÊìæÅAê›íË(0Ç©ÇÁ2)
	pSpin = (CSpinButtonCtrl*)GetDlgItem(IDC_DISK_SIZES);
	ASSERT(pSpin);
